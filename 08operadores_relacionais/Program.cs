// Variáveis
int numero1 = 5;
int numero2 = 8;

// Maior
System.Console.WriteLine(numero1 > numero2);

// Maior ou igual
System.Console.WriteLine(numero1 >= numero2);

// Menor
System.Console.WriteLine(numero1 < numero2);

// Menor ou igual
System.Console.WriteLine(numero1 <= numero2);

// Igual
System.Console.WriteLine(numero1 == numero2);

// Diferente
System.Console.WriteLine(numero1 != numero2);